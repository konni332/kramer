// white piece on sq → mirror to get PST index
// rank 0 (a1..h1) should map to the BOTTOM of the table (visually rank 1)
#[inline(always)]
pub(super) fn white_pst_index(sq: u8) -> usize {
    // mirror vertically: rank 7 becomes rank 0 in the table
    let rank = sq / 8;
    let file = sq % 8;
    ((7 - rank) * 8 + file) as usize
}

#[inline(always)]
pub(super) fn black_pst_index(sq: u8) -> usize {
    // black is already mirrored — sq ^ 56 flips the rank
    (sq ^ 56) as usize
}
